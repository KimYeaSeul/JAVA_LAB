package variable;

public class BinaryTest {
	
	public static void main(String[] args) {
		
		int num = 10;
		int bNum = 0b1010; // 2진법
		int oNum = 012; // 8진법
		int xNum = 0xA; // 16진법
		
		System.out.println(num);
		System.out.println(bNum);
		System.out.println(oNum);
		System.out.println(xNum);
	}

}

 
